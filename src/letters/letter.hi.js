/**
 * src/letters/letter.hi.js
 * Hindi Letter Template
 * Context: Result declaration acknowledged, focus on assignment-related concerns
 * Input: studentData = { fullName, rollNumber, enrollmentNumber, phoneNumber, issueType }
 * Tone: Respectful, firm, student-centric
 */

const hindiLetter = (studentData) => {
  if (!studentData) return '';

  const { fullName, rollNumber, enrollmentNumber, phoneNumber, issueType = 'Both' } = studentData;

  const booksSection = `
इसके अलावा, BEG-04, BEG-05, BHD-04 और BHD-05 जैसे कई विषयों की निर्धारित पुस्तकें अभी तक सभी छात्रों को उपलब्ध नहीं हुई हैं। पुस्तकों की कमी और सीमित समय के कारण असाइनमेंट पूरी करना छात्रों के लिए बहुत मुश्किल हो रहा है।
`;

  const assignmentSection = `
हाल ही में ऑफलाइन क्लासेस की घोषणा के साथ ही अचानक बताया गया कि भाषा विषयों के असाइनमेंट बहुत कम समय में जमा करने होंगे। इतनी जल्दी सूचना मिलने के कारण न तो योजना बनाना आसान था और न ही गुणवत्तापूर्ण तैयारी करना।

स्थिति और मुश्किल तब हो जाती है जब असाइनमेंट की हार्डकॉपी जमा करना अनिवार्य कर दिया गया। दिल्ली के बाहर रहने वाले छात्रों के लिए यह बड़ा चुनौतीपूर्ण है। कई छात्रों को आने-जाने में 2-3 घंटे लग जाते हैं, और कई बार लेट पहुंचने पर भी शिक्षक या कोऑर्डिनेटर नहीं मिलते। ऐसे हालात में समय पर असाइनमेंट जमा कर पाना मुश्किल हो जाता है।
`;

  let issueText = '';
  if (issueType === 'Books') issueText = booksSection;
  else if (issueType === 'Assignment') issueText = assignmentSection;
  else if (issueType === 'Both') issueText = assignmentSection + booksSection;

  return `
माननीय महोदय,

मैं Jamia Millia Islamia के Centre for Distance and Online Education के अंतर्गत संचालित BA (ODL) कार्यक्रम का छात्र होने के नाते, यह पत्र पूरे सम्मान के साथ प्रस्तुत कर रहा हूँ।

सबसे पहले, मैं द्वितीय वर्ष BA (ODL) के परिणाम 18 दिसंबर 2025 को घोषित किए जाने के लिए धन्यवाद देना चाहता हूँ। लंबे समय से अनिश्चितता और छात्रों के निरंतर प्रयासों के बाद परिणाम घोषित होने से हमें अपनी शैक्षणिक स्थिति स्पष्ट रूप से समझने का अवसर मिला है।

परिणाम घोषणा की सराहना करते हुए, कुछ जरूरी शैक्षणिक समस्याएँ सामने आई हैं, खासकर असाइनमेंट और बैकलॉग से जुड़ी, जिन पर ध्यान देने की आवश्यकता है।

पिछले दो सालों में BA (ODL) प्रोग्राम में समय-सारणी लगातार नहीं रही। पहले वर्ष में लगभग 15–17 महीने लगे, जबकि दूसरे वर्ष को केवल 7–8 महीनों में पूरा करना पड़ा। इस वजह से छात्र अचानक असाइनमेंट की सूचना पाने के लिए तैयार नहीं थे।

${issueText}

इसलिए मैं विनम्रतापूर्वक निम्न अनुरोध करता हूँ:

${issueType !== 'Books' ? `1) असाइनमेंट जमा करने की तारीख बढ़ाई जाए।  
2) हार्डकॉपी अनिवार्यता पर पुनर्विचार किया जाए और ऑनलाइन या वैकल्पिक सबमिशन की सुविधा सुनिश्चित की जाए।` : ''}
${issueType !== 'Assignment' ? `3) अपूर्ण अध्ययन सामग्री के वितरण की समस्या का शीघ्र समाधान किया जाए तथा सभी विषयों की पुस्तकें समय पर उपलब्ध कराई जाएँ।` : ''}
${issueType === 'Both' ? `1) असाइनमेंट जमा करने की तारीख बढ़ाई जाए।  
2) हार्डकॉपी अनिवार्यता पर पुनर्विचार किया जाए और ऑनलाइन या वैकल्पिक सबमिशन की सुविधा सुनिश्चित की जाए।  
3) अपूर्ण अध्ययन सामग्री के वितरण की समस्या का शीघ्र समाधान किया जाए तथा सभी विषयों की पुस्तकें समय पर उपलब्ध कराई जाएँ।` : ''}

मेरे विवरण आपके संदर्भ हेतु नीचे दिए जा रहे हैं:

- नाम: ${fullName}  
- रोल नंबर: ${rollNumber}  
- नामांकन नंबर: ${enrollmentNumber}  
- संपर्क नंबर: ${phoneNumber}  

मैं यह पत्र पूरे सम्मान और विश्वास के साथ प्रस्तुत कर रहा हूँ और आशा करता हूँ कि इन मुद्दों पर सहानुभूतिपूर्वक विचार करते हुए छात्रों के हित में उचित निर्णय लिया जाएगा।

भवदीय,  
${fullName}
`.trim();
};

export default hindiLetter;
